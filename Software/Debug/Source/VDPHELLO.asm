nabu_vdp_data	equ	0xBE	; VDP Data Port
nabu_vdp_addr	equ	0xBF	; VDP Address Port


; Program start
	org	0x0000
	
	; Print "hello" message
start:	
	ld	a,0x42
	ld	bc,0x1234
	inc	h
	inc	l
	di
	ld	b,0x69
	out	(0xFF),a
	inc	a
	inc	b
	inc	c
	inc	d
	inc	e
	inc	h
	inc	l
	ld	sp,0xFFFF

	; Set up write address
	ld	bc,0x4800
	call	tms_addr
	
	; Write it in!
	ld	hl,tms_hello
	ld	bc,24*40
0$:	ld	a,(hl)
	out	(nabu_vdp_data),a
	push	af
	pop	af
	dec	bc
	inc	hl
	ld	a,b
	or	c
	jp	nz,0$
1$:	jp	1$




tms_addr:
	in	a,(nabu_vdp_addr)
	ld	a,c
	out	(nabu_vdp_addr),a
	ld	a,b
	out	(nabu_vdp_addr),a
	ret
	

