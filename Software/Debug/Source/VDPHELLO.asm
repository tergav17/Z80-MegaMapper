nabu_vdp_data	equ	0x98	; VDP Data Port
nabu_vdp_addr	equ	0x99 	; VDP Address Port

draw_buffer	equ	0xC000


; Program start
	org	0x0000
	
	; Do stuff with the VDP
start:	
	ld	a,0x50
	out	(0xA8),a
	ld	a,0x42
	ld	bc,0x1234
	inc	h
	inc	l
	di
	ld	b,0x69
	out	(0xFF),a
	inc	a
	inc	b
	inc	c
	inc	d
	inc	e
	inc	h
	inc	l
	ld	sp,0xFFFF
	
	; Move tms_hello to the draw buffer
	ld	hl,tms_hello
	ld	de,draw_buffer
	ld	bc,40*24
	ldir
	
	; Draw a little pattern
	ld	hl,draw_buffer+(tms_foobar-tms_hello)
	xor	a
69$:	ld	(hl),a
	inc	hl
	inc	a
	jp	nz,69$
	

dowrite:	
	; Grab key matrix values
	ld	a,0x00
	out	(0xAA),a
	in	a,(0xA9)
	call	tohex
	ld	(0xC000 + (40*1)),de
	ld	a,0x01
	out	(0xAA),a
	in	a,(0xA9)
	call	tohex
	ld	(0xC000 + (40*2)),de
	ld	a,0x02
	out	(0xAA),a
	in	a,(0xA9)
	call	tohex
	ld	(0xC000 + (40*3)),de
	ld	a,0x03
	out	(0xAA),a
	in	a,(0xA9)
	call	tohex
	ld	(0xC000 + (40*4)),de
	ld	a,0x04
	out	(0xAA),a
	in	a,(0xA9)
	call	tohex
	ld	(0xC000 + (40*5)),de
	ld	a,0x05
	out	(0xAA),a
	in	a,(0xA9)
	call	tohex
	ld	(0xC000 + (40*6)),de
	ld	a,0x06
	out	(0xAA),a
	in	a,(0xA9)
	call	tohex
	ld	(0xC000 + (40*7)),de
	ld	a,0x07
	out	(0xAA),a
	in	a,(0xA9)
	call	tohex
	ld	(0xC000 + (40*8)),de
	ld	a,0x08
	out	(0xAA),a
	in	a,(0xA9)
	call	tohex
	ld	(0xC000 + (40*9)),de


	; Set up write address
	ld	bc,0x4800
	call	tms_addr
	
	; Write it in!
	ld	hl,draw_buffer
	ld	bc,24*40
0$:	ld	a,(hl)
	out	(nabu_vdp_data),a
	push	af
	pop	af
	dec	bc
	inc	hl
	ld	a,b
	or	c
	jp	nz,0$
	jp	dowrite


; ----------------------
; ******** Misc ********
; ----------------------
	
; Converts the value into an 8 bit hex number
; A = Number to convert
;
; Returns DE = result
; Uses: AF, DE
tohex:	ld	d,a
	call	0$
	ld	e,a
	ld	a,d
	call	1$
	ld	d,a
	ret
	
0$:	rra
	rra
	rra
	rra
1$:	or	0xF0
	daa
	add	a,0xA0
	adc	a,0x40
	ret



tms_addr:
	in	a,(nabu_vdp_addr)
	ld	a,c
	out	(nabu_vdp_addr),a
	ld	a,b
	out	(nabu_vdp_addr),a
	ret
	

